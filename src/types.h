// Orion_'s Common C Types

#ifndef	_OTYPES_H_
#define	_OTYPES_H_

#ifndef	bool
typedef	unsigned char	bool;
#endif

#ifndef	false
#define	false			(0)
#endif

#ifndef	true
#define	true			(1)
#endif

#ifndef	NULL
#define	NULL			((void *)0)
#endif

typedef	unsigned char	u8;
typedef	unsigned short	u16;
typedef	unsigned long	u32;
typedef	signed char		s8;
typedef	signed short	s16;
typedef	signed long		s32;

#endif
